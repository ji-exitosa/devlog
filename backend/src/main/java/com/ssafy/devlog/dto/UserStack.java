package com.ssafy.devlog.dto;

public class UserStack {
	private int seq;
	private int seq_user;
	private String stack;
	private String stack_img_url;
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public int getSeq_user() {
		return seq_user;
	}
	public void setSeq_user(int seq_user) {
		this.seq_user = seq_user;
	}
	public String getStack() {
		return stack;
	}
	public void setStack(String stack) {
		this.stack = stack;
	}
	public String getStack_img_url() {
		return stack_img_url;
	}
	public void setStack_img_url(String stack_img_url) {
		this.stack_img_url = stack_img_url;
	}
	
	
}
